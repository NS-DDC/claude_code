function djb2Hash(s: string): number {
  let hash = 5381;
  for (let i = 0; i < s.length; i++) {
    hash = ((hash << 5) + hash + s.charCodeAt(i)) & 0x7fffffff;
  }
  return hash >>> 0;
}

export function getDailyCard(seed: string): { card: TarotCard; isReversed: boolean } {
  const hash = djb2Hash(seed);
  const card = tarotCards[hash % tarotCards.length];
  const isReversed = (djb2Hash(seed + 'rev')) % 7 < 2;
  return { card, isReversed };
}
